public class AverageOfThree {
    public static float avgthree(int a, int b, int c){
        float avg =(a + b + c)/3f ;
        return avg;

    }
    public static void main(String args[]){
        int  num1 = 2;
        int  num2 = 5;
        int  num3 = 3;
        System.out.println(avgthree(num1,num2,num3));;
    }
}
